package Algorithms.FifthPart.ListInsertionSort;

public class Link {

    public long dData; // Data
    public Link next; // Next item in the list

    public Link(long dd) // Constructor
    { dData = dd; }

    public long getdData() { // Getter
        return dData;
    }

    public Link getNext() { // Getter
        return next;
    }
}
